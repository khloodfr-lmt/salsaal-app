# ============================================================
#  Webhook – يستقبل رسائل واتساب
# ============================================================
@app.route("/whatsapp", methods=["POST"])
def whatsapp():
    sender  = request.form.get("From", "")   # مثل: whatsapp:+9665XXXXXXXX
    msg_in  = request.form.get("Body", "").strip()

    if not msg_in:
        return "", 204

    # أنشئي محادثة جديدة إذا المستخدمة جديدة
    if sender not in conversations:
        conversations[sender] = []
        # أضيفي رسالة ترحيب تلقائية في أول مرة
        welcome = (
            "أهلاً وسهلاً! 💕\n"
            "أنا *Beauty Agent* — مساعدتك الجمالية الذكية 💄\n\n"
            "عشان أعطيكِ نصائح مخصصة لكِ، محتاجة أعرف:\n\n"
            "1️⃣ *لون بشرتك:*\n"
            "فاتحة جداً / فاتحة / وسطية / زيتونية / بنية / داكنة\n\n"
            "2️⃣ *نوع بشرتك:*\n"
            "دهنية / جافة / مختلطة / حساسة / عادية\n\n"
            "أرسليلي المعلومتين وأبدأ معك فوراً! 🌸"
        )
        resp = MessagingResponse()
        resp.message(welcome)
        # سجلي الرسالة الواردة بعد الترحيب
        conversations[sender].append({"role": "user", "content": msg_in})
        return str(resp)

    # أضيفي رسالة المستخدمة للتاريخ
    conversations[sender].append({"role": "user", "content": msg_in})

    # حدي طول التاريخ (آخر 20 رسالة)
    history = conversations[sender][-20:]

    # اطلبي رد من Claude
    try:
        result = claude.messages.create(
            model="claude-opus-4-5",
            max_tokens=600,
            system=SYSTEM_PROMPT,
            messages=history
        )
        reply = result.content[0].text
    except Exception as e:
        reply = "عذراً، في مشكلة تقنية بسيطة 🌸 جربي مرة ثانية بعد شوي."
        print(f"Claude error: {e}")

    # احفظي الرد
    conversations[sender].append({"role": "assistant", "content": reply})

    # ردي على المستخدمة
    resp = MessagingResponse()
    resp.message(reply)
    return str(resp)

